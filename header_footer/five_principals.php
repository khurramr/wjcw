    <button class="accordion">TAUWHEED</button>
    <div class="panel">
      <p>There is only one God - Eternal, Self-sufficient; He has no beginning or end; He is Omnipotent, has power over everything and every affair; He is Omniscient, knows everything, nothing is ever a secret from Him. He has His own discretion in all affairs, does not do anything under compulsion. He is All-perceiving, All-Hearing, All-Seeing and Omnipresent, He sees and hears everything though He has neither eyes no ears. He has no partner or colleague, nor has He any son, daughter or wife. He is neither made nor composed of any material substance. He has no body nor is He confined to a place. He is not afflicted by anything related to body: He is not governed by time, space, change or things like that. He is not visible, He has not been seen, and will never be seen either in this world or the hereafter. His attributes are not separate from His person.</p>
    </div>

    <button class="accordion">ADAL</button>
    <div class="panel">
      <p>       Allah is just; He does not do injustice to anyone. He has ordered us to do justice to our fellow creatures - but He Himself treats us not only with justice but with grace. He created us for His worship, in order that we may attain spiritual perfection through it. That spiritual perfection enables us to reach near to Allah.
           For this purpose, He has given us freedom of will and choice. When we choose, by our own free will the Straight Path (prescribed by Allah) we are assured in the next world of everlasting happiness and glory by Grace of Allah.
    </p>
    </div>

    <button class="accordion">NABUWAT</button>
    <div class="panel">
      <p>To show us the Straight Path, Allah has been sending His Representatives to the mankind. They are called nabii (Prophet) and rasul (messenger). (Plural: anbiya'; rusul or mursaleen).

            There came 124,000 prophets and messengers, beginning with Adam (a.s.) and ending with Muhammad (s.a.w.a.). All the prophets were ma'sum (sinless, infallible; Plural; ma'sumeem). Muhammad, the Holy Prophet of Islam, is the Final Prophet; anybody claiming prophet hood after him is liar and imposter.
    </p>
